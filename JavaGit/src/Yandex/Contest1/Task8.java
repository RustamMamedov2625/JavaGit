package Yandex.Contest1;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Task8 {
    public static void calcDistance() throws IOException {
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));

        String[] strNumbersArray = reader.readLine().split(" ");

        long L = Long.parseLong(strNumbersArray[0]);
        long x1 = Long.parseLong(strNumbersArray[1]);
        long v1 = Long.parseLong(strNumbersArray[2]);
        long x2 = Long.parseLong(strNumbersArray[3]);
        long v2 = Long.parseLong(strNumbersArray[4]);

        double t = 0;
        if(v1 == v2 && v1 == 0 && (x1 != x2 || x1 + x2 != L)){
            System.out.println("No");
        }
        else if(x1 == x2 || x1 + x2 == L){
            System.out.println("YES");
            System.out.printf("%.10f%n", t);
            System.out.println("1");
        }
        else if(v2 <= 0 && v1 >= 0 && x2 > x1){
            t = (double)(x2 - x1) / (double)(Math.abs(v1) + Math.abs(v2));
            System.out.println("YES");
            System.out.printf("%.10f%n", t);

            System.out.println("2");
        }
        else if(v2 <= 0 && v1 >= 0 && x2 < x1){
            t = (double)(L + x2 - x1) / (double)(Math.abs(v1) + Math.abs(v2));
            System.out.println("YES");
            System.out.printf("%.10f%n", t);

            System.out.println("3");
        }
        else if(v1 <= 0 && v2 >= 0 && x1 > x2){
            t = (double)(x1 - x2) / (double)(Math.abs(v1) + Math.abs(v2));
            System.out.println("YES");
            System.out.printf("%.10f%n", t);

            System.out.println("4");
        }
        else if(v1 <= 0 && v2 >= 0 && x1 < x2){
            t = (double)(L - x2 + x1) / (double)(Math.abs(v1) + Math.abs(v2));
            System.out.println("YES");
            System.out.printf("%.10f%n", t);

            System.out.println("5");
        }
        else if (v1 == v2 ) {
            if(v1 < 0) {
                t = (double) (x2 + x1) / (double) (Math.abs(v1) + Math.abs(v2));
                System.out.println("YES");
                System.out.printf("%.10f%n", t);

                System.out.println("6");
            }
            else {
                t = (double)(L - x1 - x2) / (double) (v1 + v2);
                System.out.println("YES");
                System.out.printf("%.10f%n", t);
            }
        }


    }
}

