package Yandex.Contest1;

import java.io.BufferedReader;
import java.io.IOException;
import java.io.InputStreamReader;

public class Task4 {
    public static void countFreeCells() throws IOException {
        BufferedReader reader = new BufferedReader(new InputStreamReader(System.in));
        char[][] chessDesk = new char[8][8];
        int count = 0;//Считывание количество свободных *


        for (int i = 0; i < 8; i++) {//считывание строк в массив
            String line = reader.readLine();
            for (int j = 0; j < 8; j++) {
                chessDesk[i][j] = line.charAt(j);
                if (chessDesk[i][j] == '*')
                    count++;
            }
        }
        //Основа алгоритма
        if (count == 0) {
            System.out.println(count);
        } else {
            count = 0;
        }
    }
}

